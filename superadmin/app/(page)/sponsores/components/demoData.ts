export const calculatePromotionStats = (
  campaigns: Campaign[],
  coupons: Coupon[],
  banners: Banner[],
  featuredItems: FeaturedItem[],
  notifications: NotificationCampaign[]
): PromotionStats => {
  const activeCampaigns = campaigns.filter(c => c.status === 'active').length;
  const activeCoupons = coupons.filter(c => c.isActive).length;
  const activeBanners = banners.filter(b => b.isActive).length;
  const featuredSellers = featuredItems.filter(f => f.type === 'seller' && f.status === 'active').length;
  const featuredProducts = featuredItems.filter(f => f.type === 'product' && f.status === 'active').length;
  const pendingNotifications = notifications.filter(n => ['draft', 'scheduled'].includes(n.status)).length;
  const sentNotifications = notifications.filter(n => n.status === 'sent').length;
  const totalCouponUsage = coupons.reduce((sum, c) => sum + c.usedCount, 0);
  const totalDiscountGiven = campaigns.reduce((sum, c) => sum + c.usedBudget, 0);

  return {
    totalCampaigns: campaigns.length,
    activeCampaigns,
    totalCoupons: coupons.length,
    activeCoupons,
    totalBanners: banners.length,
    activeBanners,
    featuredSellers,
    featuredProducts,
    pendingNotifications,
    sentNotifications,
    totalCouponUsage,
    totalDiscountGiven,
  };
};
