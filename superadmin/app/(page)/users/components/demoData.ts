export const calculateUserStats = (users: User[]): UserStats => {
  const now = new Date();
  const thisMonth = new Date(now.getFullYear(), now.getMonth(), 1);

  return {
    totalUsers: users.length,
    activeUsers: users.filter((u) => u.status === 'active').length,
    newUsersThisMonth: users.filter((u) => new Date(u.joinedAt) >= thisMonth).length,
    totalRevenue: users.reduce((sum, u) => sum + u.totalSpent, 0),
    avgOrderValue: users.reduce((sum, u) => sum + u.totalSpent, 0) / users.reduce((sum, u) => sum + u.totalOrders, 0) || 0,
    totalOrders: users.reduce((sum, u) => sum + u.totalOrders, 0),
  };
};
